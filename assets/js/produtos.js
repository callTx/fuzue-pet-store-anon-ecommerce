if (category != null){
    var tituloCategoria = formatString(category);

    titleH2.textContent = tituloCategoria;
    filteredProducts = products.filter(product => product.category === category);
}else{
  if (favorito != null){

    filteredProducts = products.filter(product => product.favorito === favorito);
    if (titlesection != null){
      var tituloCategoria = formatString(titlesection);

      titleH2.textContent = tituloCategoria;
    }
  }
}

filteredProducts.forEach((product) => {
  // parametrosFormatados =  replaceSpacesWithPercent20(product.parametros);
  const productHTML = generateProductHTML(product);
  productGridDiv.innerHTML += productHTML;
});

function replaceSpacesWithPercent20(urlParameters) {
  // Replace each blank space with %20
  return urlParameters.replace(/ /g, '%20');
}

